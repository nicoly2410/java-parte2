public class pessoa {
  private String nome;
  private int idade;
  private String cidade;

  // Getter para o nome_________________________
  public String getNome() {
    return nome;
  }

  // Setter para o nome_________________________
  public void setNome(String nome) {
    this.nome = nome;
  }

  // Getter para a idade_________________________
  public int getIdade() {
    return idade;
  }

  // Setter para a idade_________________________
  public void setIdade(int idade) {
    this.idade = idade;
  }

  public String getCidade() {
    return cidade;
  }

  // Setter para o nome_________________________
  public void setCidade(String cidade) {
    this.cidade = cidade;
  }

  // Getter para o salario
  public void getSalario() {
    return salario;
  }

  // Setter para o salario
  public void setSalario(int sala) {
    this.salario = salario;
  }
}